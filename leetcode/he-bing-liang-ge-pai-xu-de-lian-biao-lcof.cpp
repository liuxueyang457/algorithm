// 剑指 Offer 25. 合并两个排序的链表
// https://leetcode.cn/problems/he-bing-liang-ge-pai-xu-de-lian-biao-lcof/description/

/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
  ListNode* mergeTwoLists(ListNode* l1, ListNode* l2) {
    if (!l1) return l2;
    if (!l2) return l1;
    LNP res = new LN(0);
    LNP cur = res;

    while (l1 || l2) {
      if (l1 && l2) {
        if (l1->val <= l2->val) {
          cur->next = l1;
          cur = cur->next;
          l1 = l1->next;
        } else {
          cur->next = l2;
          cur = cur->next;
          l2 = l2->next;
        }
      } else if (l1) {
        cur->next = l1;
        cur = cur->next;
        l1 = l1->next;
      } else if (l2) {
        cur->next = l2;
        cur = cur->next;
        l2 = l2->next;
      }
    }

    if (cur) cur->next = nullptr;
    return res->next;
  }
};
